package com.hwaipy.science.polarizationcontrol.m1;

/**
 *
 * @author HwaipyLab
 */
public class M1ProcessException extends Exception {

    public M1ProcessException() {
    }

    public M1ProcessException(String message) {
        super(message);
    }

    public M1ProcessException(String message, Throwable cause) {
        super(message, cause);
    }

    public M1ProcessException(Throwable cause) {
        super(cause);
    }

    public M1ProcessException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
